def seed_novels():
    """展示用小説データを挿入する"""
    db = next(get_db())

    print("展示用小説データを作成中...")

    # 既存のデータを削除
    db.query(Chapter).delete()
    db.query(Novel).delete()
    db.commit()

    created_novel_count = 0
    created_chapter_count = 0

    for novel_data in EXHIBITION_NOVEL_DATA:
        novel_id = str(uuid.uuid4())

        # 小説を作成
        new_novel = Novel(
            id=novel_id,
            title=novel_data["title"],
            description=novel_data["description"],
            background=novel_data["background"],
            characters=novel_data["characters"],
            relationships=novel_data["relationships"],
            style=novel_data["style"],
            outline=novel_data["outline"],
            created_at=datetime.utcnow(),
            updated_at=datetime.utcnow(),
        )

        db.add(new_novel)
        created_novel_count += 1
        print(f"  作成: {novel_data['title']}")

        # 章を作成
        for chapter_data in novel_data["chapters"]:
            chapter_id = str(uuid.uuid4())

            new_chapter = Chapter(
                id=chapter_id,
                novel_id=novel_id,
                title=chapter_data["title"],
                content=chapter_data["content"],
                outline="",
                order=chapter_data["order"],
                created_at=datetime.utcnow(),
                updated_at=datetime.utcnow(),
            )

            db.add(new_chapter)
            created_chapter_count += 1
            print(f"    - {chapter_data['title']}")

    db.commit()
    print(f"\n完了！{created_novel_count}件の展示用小説と{created_chapter_count}件の章を作成しました。")
    print("\n相互に関連する3作品で、小説家レベルの文書力を実現しました。")


if __name__ == "__main__":
    seed_novels()
